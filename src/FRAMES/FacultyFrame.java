    private void getFaculty(){
     try {
            Vector v = DBManager.getFaculty();
            facultyNameList.setListData(v);
           
        } catch (Exception ex) {
            Logger.getLogger(DepartmentFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
   }
    
    private void addRecord(){
           String facName = this.facNameTextField.getText();
           String remarks = this.remarksTextArea.getText();
        try {
            int row = DBManager.addFaculty(facName, remarks);
            if(row>0){
                JOptionPane.showMessageDialog(null,"RECORD ADDED SUCCESSFULLY");
               clear();
               getFaculty();
            }else{
                JOptionPane.showMessageDialog(null,"Something went wrong");
            }
            
        } catch (Exception ex) {
            Logger.getLogger(FacultyFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
            
    private void clear(){
    facultyIdTextField.setText("");    
    remarksTextArea.setText("");
    facNameTextField.setText("");
    } 
    
    
    private void updateRecord(){
      
        FacultyBean  bean = (FacultyBean) facultyNameList.getSelectedValue();  
       
        if(bean==null)return;
        
        try {
             //int row = DBManager.updateFaculty(bean.getFacId());
            int row = DBManager.updateFaculty(bean.getFacId(),facNameTextField.getText(),remarksTextArea.getText());
            if(row>0){
                JOptionPane.showMessageDialog(null,"RECORD UPDATE SUCCESSFULLY");
               clear();
               getFaculty();
            }else{
                JOptionPane.showMessageDialog(null,"Something went wrong");
            }
            
        } catch (Exception ex) {
            Logger.getLogger(FacultyFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    
    }  
    
    private void deleteRecord(){
      
        FacultyBean  bean = (FacultyBean) facultyNameList.getSelectedValue();  
       if(bean==null)return;
        try {
            
            int row = DBManager.deleteFaculty(bean.getFacId());
            if(row>0){
                JOptionPane.showMessageDialog(null,"RECORD DELETED SUCCESSFULLY");
               clear();
               getFaculty();
            }else{
                JOptionPane.showMessageDialog(null,"Something went wrong");
            }
            
        } catch (Exception ex) {
            Logger.getLogger(FacultyFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    
    }  

